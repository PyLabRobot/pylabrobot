class TecanTipCarrier(TipCarrier, TecanResource):
  """Base class for Tecan tip carriers."""

  def __init__(
    self,
    name: str,
    size_x: float,
    size_y: float,
    size_z: float,
    off_x: float,
    off_y: float,
    sites: Optional[Dict[int, ResourceHolder]] = None,
    category="tecan_tip_carrier",
    model: Optional[str] = None,
  ):
    super().__init__(
      name,
      size_x,
      size_y,
      size_z,
      sites,
      category=category,
      model=model,
    )

    self.off_x: float = off_x
    self.off_y: float = off_y


