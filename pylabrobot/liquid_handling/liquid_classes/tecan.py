def from_str(s: str) -> Optional[Liquid]:
  """ Parses a Tecan liquid class name and creates a Liquid object. """

  m = re.match(r"(\w+) free dispense$", s)
  if m is None:
    return None

  lc = m.group(1)
  if lc in {"Ethanol", "Serum"}:
    lc += " 100%"
  return Liquid(lc)


